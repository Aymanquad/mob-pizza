/**
 * Update order status (admin/host can update any order, customers can only update their own)
 */
exports.updateOrderStatus = async (req, res) => {
  try {
    const { phone, orderId } = req.params;
    const { status } = req.body;

    if (!status) {
      return res.status(400).json({
        success: false,
        message: 'Status is required',
      });
    }

    const validStatuses = ['pending', 'confirmed', 'preparing', 'ready', 'picked-up', 'delivered', 'cancelled'];
    if (!validStatuses.includes(status)) {
      return res.status(400).json({
        success: false,
        message: `Invalid status. Must be one of: ${validStatuses.join(', ')}`,
      });
    }

    // Find user by phone, email, or googleId
    let user = null;
    let query = {};
    
    // Check if identifier is an email
    if (phone.includes('@')) {
      query = { email: phone.toLowerCase() };
      user = await User.findOne(query);
    }
    
    // If not found and not an email, try phone
    if (!user && !phone.includes('@')) {
      query = { phone };
      user = await User.findOne(query);
    }
    
    // If still not found, try googleId (for long numeric strings)
    if (!user && phone.length > 15 && /^\d+$/.test(phone)) {
      query = { googleId: phone };
      user = await User.findOne(query);
    }
    
    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found',
      });
    }

    // Build query - CRITICAL SECURITY FIX: Only admin/delivery can update any order
    // Default to customer filtering for safety
    const orderQuery = { _id: orderId };
    const isAdminOrDelivery = user.role === 'admin' || user.role === 'delivery';
    
    if (!isAdminOrDelivery) {
      // Customer or any other/undefined role - ONLY update their own orders
      orderQuery.customer = user._id;
      console.log(`[order] Customer user (role: ${user.role || 'undefined'}) - can only update own orders, filtering by customer._id: ${user._id}`);
    } else {
      console.log(`[order] Admin/Delivery user (${user.role}) - can update any order`);
    }

    const order = await Order.findOne(orderQuery);

    if (!order) {
      return res.status(404).json({
        success: false,
        message: 'Order not found or you do not have permission to update this order',
      });
    }

    order.orderStatus = status;
    if (status === 'delivered') {
      order.actualDeliveryTime = new Date();
    }

    await order.save();

    // Populate customer info for response
    await order.populate('customer', 'firstName lastName phone');

    return res.status(200).json({
      success: true,
      message: 'Order status updated',
      data: order,
    });
  } catch (error) {
    console.error('[order] error updating order status:', error);
    return res.status(500).json({
      success: false,
      message: 'Failed to update order status',
      error: error.message,
    });
  }
};

